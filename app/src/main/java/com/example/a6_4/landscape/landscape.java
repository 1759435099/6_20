package com.example.a6_4.landscape;

public class landscape {

    private String name;

    private int imageId;

    public landscape(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}